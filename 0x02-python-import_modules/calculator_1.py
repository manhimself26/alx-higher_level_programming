#!/usr/bin/python3
def add(a, b):
    """my addition function

    Args:
        a: first integer
        b: second integer

    Returns:
        The value of a + b
    """
    return (a + b)


def sub(a, b):
    """my subtraction function

    Args:
        a: first integer
        b: second integer

     Returns:
        the value a - b
    """
    return (a - b)


def mul(a, b):
    """my multiplication function

    args:
         a: first integer
         b: second integer

     Returns:
        the value a * b
    """
    return (a * b)


def div(a, b):
    """my division function

    args:
        a: first integer
        b: second integer

     Returns:
        the value of a / b
    """
    return (a / b)
